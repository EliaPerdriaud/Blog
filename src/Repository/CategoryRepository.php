    public function findCatWithPost():array {
        $date= new DateTime();
        return $this->createQueryBuilder('category')
        ->join('category.posts','posts')
        ->where('posts.id>0')
        ->andWhere('posts.publishedAt<:date')
        ->setParameter('date',$date)
        ->orderBy('category.name')
        ->getQuery()
        ->getResult();
    }


